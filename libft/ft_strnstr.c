#include "libft.h"

char	*ft_strnstr(const char *haystack, const char *needle, size_t len)
// assuming returns haystack for needle=""
// this is inefficient because it continues searching when
// i > len(haystack) - len(needle) which is impossible
{
	size_t	i;
	size_t	j;

	if (!haystack || !needle)
		return (NULL);
	if (!needle[0])
		return ((char *)haystack);
	i = 0;
	while (haystack[i] && i < len)
	{
		j = 0;
		while (haystack[i + j] && needle[j]
			&& i + j < len && haystack[i + j] == needle[j])
			j++;
		if (!needle[j])
			return ((char *)(haystack + i));
		i++;
	}
	return (NULL);
}
