#include "libft.h"

size_t	ft_strlcpy(char *dst, const char *src, size_t size)
// not sure if return value correct, i couldnt find the man page
// might be -1 if truncated (strtcpy)
{
	size_t	i;

	i = 0;
	if (size > 0)
	{
		while (src[i] && i < (size - 1))
		{
			dst[i] = src[i];
			i++;
		}
		dst[i] = 0;
	}
	while (src[i])
		i++;
	return (i);
}
