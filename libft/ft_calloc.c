#include "libft.h"

void	*ft_calloc(size_t count, size_t size)
// might be bzero count instead of count * size
{
	void	*ptr;

	ptr = (void *)malloc(count * size);
	if (!ptr)
		return (NULL);
	ft_bzero(ptr, count * size);
	return (ptr);
}
